# Basic Arithmetic Operations Program

num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2
floor_division = num1 // num2 #(divides two numbers and returns the largest integer)
exponential = num1 ** num2 #(num1 to the power of num2)
modulus = num1 % num2 #( returns the remainder of the division)

print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Floor division:",floor_division)
print("Exponential:",exponential)
print("Modulus:",modulus)
